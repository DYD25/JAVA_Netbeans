    private void BtnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCalcularActionPerformed
        double Vunitario1 = Double.parseDouble(TextValUnita1.getText());
        double Vunitario2 = Double.parseDouble(TextValUnita2.getText());        
        double Vunitario3 = Double.parseDouble(TextValUnita3.getText());
        double Vunitario4 = Double.parseDouble(TextValUnita4.getText());
        double Vunitario5 = Double.parseDouble(TextValUnita5.getText());
        
        double cantidad1 = Double.parseDouble(TextCantidad1.getText());        
        double cantidad2 = Double.parseDouble(TextCantidad2.getText());        
        double cantidad3 = Double.parseDouble(TextCantidad3.getText());        
        double cantidad4 = Double.parseDouble(TextCantidad4.getText());        
        double cantidad5 = Double.parseDouble(TextCantidad5.getText());

        double resul1= Vunitario1 * cantidad1;       
        double resul2= Vunitario2 * cantidad2;        
        double resul3= Vunitario3 * cantidad3;        
        double resul4= Vunitario4 * cantidad4;       
        double resul5= Vunitario5 * cantidad5;
        
        
        double sumatoria= resul1 + resul2 +resul3 + resul4 + resul5;
        
        TextVlrTotal1.setText(""+ resul1);        
        TextVlrTotal2.setText(""+ resul2);
        TextVlrTotal3.setText(""+ resul3);        
        TextVlrTotal4.setText(""+ resul4);        
        TextVlrTotal5.setText(""+ resul5);
        
        TextTotal.setText(""+ sumatoria);       
        
        TextSalida.setText( "  "+ TextCliente.getText() + ".  El total de su compra fue : "+ sumatoria);

dyd.setText("Â©D&D");
    }//GEN-LAST:event_BtnCalcularActionPerformed

